package master.dto;

public class CustomerDto {

	private String cid;
	private String cname;
	private String cphno;
	private String cemail;
	private String cpanno;
	private String cadhar;
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCphno() {
		return cphno;
	}
	public void setCphno(String cphno) {
		this.cphno = cphno;
	}
	public String getCemail() {
		return cemail;
	}
	public void setCemail(String cemail) {
		this.cemail = cemail;
	}
	public String getCpanno() {
		return cpanno;
	}
	public void setCpanno(String cpanno) {
		this.cpanno = cpanno;
	}
	public String getCadhar() {
		return cadhar;
	}
	public void setCadhar(String cadhar) {
		this.cadhar = cadhar;
	}
	
	
}
